#include <iostream>

int main()
{
    printf("\"%s\"의 출석점수 계산 \n", "강병익");
    printf("-------------------------\n");
    printf("%s %11d %s\n", "수업시간", 60, "시간");
    printf("%s %11d %s\n", "결석시간", 2, "시간");
    printf("%s %13.1f %%\n", "출석률", (58.0 / 60.0) * 100.0);
    printf("%s %11.1f %s\n", "출석점수", (58.0 / 60.0) * 15, "점");
}